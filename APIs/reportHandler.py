"""
reportHandler API

Behavior Overview:
-----------------
This Lambda manages report generation for individual jobs. Each job gets ONE unique report.
Reports are generated by triggering an external n8n workflow that processes preprocessed 
ad data (JSON and image2text extractions) and returns a comprehensive analysis report.

API Endpoint: https://<YOUR-AWS-API>/dev/jobs/{jobId}/report-status
Method: GET

API input:

{
    "pathParameters": {
        "jobId": str      # Unique job identifier
    }
}

All API output scenarios:

1. Report Generation Started (First Call)
   {
       "reportStatus": 1,
       "message": "Report generation in progress - please continue polling"
   }

2. Success - Report Already Exists  
   Status Code: 200  
   {
       "reportStatus": 2,
       "reportPath": str,       # Existing S3 key for the report
       "message": "Report already exists"
   }

3. Waiting - Scraper Not Complete  
   Status Code: 200  
   {
       "reportStatus": 0,
       "message": "Scraper not completed yet",
       "scraperStatus": int
   }

4. Waiting - Image2Text In Progress  
   {
       "reportStatus": 1,
       "message": "Waiting for image extraction to complete - please continue polling",
       "image2textStatus": 1
   }

5. Retrying - Workflow or Data Check  
   Status Code: 200  
   {
       "reportStatus": 0,
       "message": str,
       "retrying": true,
       "retryCount": int,           # Workflow retry count
       "dataCheckRetryCount": int   # Data availability retry count
   }

6. Completed - Empty or Successful Report  
   Status Code: 200  
   {
       "reportStatus": 2,
       "reportPath": str,         # S3 key of generated report (if any)
       "message": str
   }

7. Failed - Permanent or Fatal Error  
   Status Code: 200  
   {
       "reportStatus": -1,
       "message": str,
       "permanent": true,
       "retryCount": int
   }

8. Error - Internal or Database Failure  
   Status Code: 500  
   {
       "error": str,
       "message": str
   }

Key Points:
1. **One Report Per Job**: Once a report is generated for a job, it cannot be regenerated.
   - Enforced via `reportPath` field - if it exists, always return that report path.
   - No locking mechanism needed since each job has its own unique report.

2. **Strict Dependency Chain**:
   - Scraping must complete successfully (`scraperStatus = 2`) before report generation
   - If scraping failed (`scraperStatus = -1`), report inherits failure
   - If scraping in progress (`scraperStatus = 0 or 1`), report waits

3. **Smart Prerequisites Check**:
   - Check `scraperStatus` first - must be 2 (completed)
   - If `numAds = 0`: Generate empty report after 3 data-check retries
   - If `image2textStatus == 1`: Set `reportStatus = 1` and wait
   - If `image2textStatus == 0 or 2`: Check actual S3 data availability
     - If data exists → proceed with report generation
     - If no data after 3 retries → generate empty report

4. **Report Generation Flow**:
   - Check if report already exists → return existing reportPath (no URL)
   - Check scraping completed successfully
   - Check if image2text is in progress → set reportStatus=1 and return (for polling)
   - Check actual S3 data availability (not just folder existence)
   - If no data: retry up to 3 times, then generate empty report
   - Set reportStatus=1 before triggering workflow (for polling)
   - Trigger n8n workflow (synchronous call with timeout)
   - Save returned report JSON to S3 at `reports/{jobId}/report.json`
   - Update job with reportStatus=2 and reportPath
   - Return reportPath (fetchReport API will generate presigned URL)

5. **Retry Logic**:
   - Data unavailability: Max 3 retries, then generate empty report
   - N8N workflow failures: Max 5 retries on transient failures
   - On permanent failure: reportStatus = -1
   - Retry count reset to 0 on successful generation

6. **Status Values**:
   - reportStatus: 0 = idle, 1 = in progress, 2 = completed, -1 = permanently failed
   - scraperStatus: 0 = idle, 1 = in progress, 2 = completed, -1 = permanently failed
   - image2textStatus: 0 = not started, 1 = in progress, 2 = completed

"""

import os
import json
import boto3
import logging
from botocore.exceptions import ClientError
from datetime import datetime, timezone, timedelta
from decimal import Decimal

import requests

# -------------------------------
# Logging Setup
# -------------------------------
logger = logging.getLogger()
logger.setLevel(logging.INFO)

# -------------------------------
# Environment Variables
# -------------------------------
BUCKET = os.environ.get("S3_BUCKET", "test-2-adspy")
JOBS_TABLE_NAME = os.environ.get("JOBS_TABLE", "jobs")
COMPANIES_TABLE_NAME = os.environ.get("COMPANIES_TABLE", "companies")
N8N_REPORT_WEBHOOK_URL = os.environ.get("N8N_REPORT_WEBHOOK_URL")
MAX_WORKFLOW_RETRIES = int(os.environ.get("MAX_WORKFLOW_RETRIES", "3"))
MAX_DATA_CHECK_RETRIES = int(os.environ.get("MAX_DATA_CHECK_RETRIES", "3"))
N8N_REQUEST_TIMEOUT = int(os.environ.get("N8N_REQUEST_TIMEOUT", "120"))  # 2 minutes

# -------------------------------
# AWS Clients
# -------------------------------
dynamodb = boto3.resource('dynamodb')
jobs_table = dynamodb.Table(JOBS_TABLE_NAME)
companies_table = dynamodb.Table(COMPANIES_TABLE_NAME)
s3 = boto3.client('s3')

# -------------------------------
# Utility Functions
# -------------------------------
def decimal_default(obj):
    """Convert Decimal to int for JSON serialization."""
    if isinstance(obj, Decimal):
        return int(obj)
    raise TypeError

def safe_name(s: str) -> str:
    """
    Convert company name into safe S3 folder name.
    Matches the naming convention used in scrape Lambda.
    """
    return "".join(c if c.isalnum() or c in (" ", ".", "_", "-") else "_" for c in s).replace(" ", "_").lower()

def parse_iso_date(date_str: str):
    """Safely parse ISO datetime string. Return None if invalid."""
    try:
        return datetime.fromisoformat(date_str)
    except (TypeError, ValueError):
        return None

def check_s3_folder_has_usable_data(prefix: str, check_json_content: bool = False) -> bool:
    """
    Check if S3 folder contains usable files.
    
    For JSON files: If check_json_content=True, also verify that JSON files
    contain actual data (not just empty "results": [] arrays).
    
    For image files: Just check existence.
    """
    try:
        response = s3.list_objects_v2(Bucket=BUCKET, Prefix=prefix, MaxKeys=100)
        
        if response.get('KeyCount', 0) == 0:
            return False
        
        # Get all file keys
        file_keys = [obj['Key'] for obj in response.get('Contents', []) 
                     if not obj['Key'].endswith('/')]  # Exclude folder markers
        
        if not file_keys:
            return False
        
        # If checking JSON content, verify at least one file has data
        if check_json_content:
            for key in file_keys:
                if key.endswith('.json'):
                    try:
                        obj = s3.get_object(Bucket=BUCKET, Key=key)
                        content = json.loads(obj['Body'].read().decode('utf-8'))
                        
                        # Check if content has actual data
                        # Common patterns: {"results": [...]} or direct array
                        if isinstance(content, dict):
                            results = content.get('results', [])
                            if results and len(results) > 0:
                                return True
                        elif isinstance(content, list) and len(content) > 0:
                            return True
                    except Exception as e:
                        logger.warning(f"[s3] Failed to read JSON file {key}: {e}")
                        continue
            
            # No JSON files with actual data found
            return False
        
        # For non-JSON checks (images), just verify files exist
        return True
        
    except Exception as e:
        logger.error(f"[s3] Error checking folder {prefix}: {e}")
        return False

def generate_empty_report(company_name: str, num_ads: int, reason: str) -> dict:
    """
    Generate an empty report when no data is available or company has no ads.
    
    Parameters:
    - company_name: str
    - num_ads: int
    - reason: str (explanation for why report is empty)
    
    Returns:
    - dict: Empty report structure
    """
    return {
        "companyName": company_name,
        "numAds": num_ads,
        "generatedAt": datetime.now(timezone.utc).isoformat(),
        "reportType": "empty",
        "reason": reason,
        "summary": f"No analysis available: {reason}",
        "analysis": None,
        "recommendations": []
    }

