# N8N Workflow Trigger
# -------------------------------
def trigger_report_workflow(company_id: str, company_name: str, job_id: str,
                           preprocessed_json_path: str, image2text_path: str) -> dict:
    """
    Trigger external n8n workflow to generate report.
    This is a FIRE-AND-FORGET call - triggers n8n workflow and returns immediately.
    The n8n workflow will update DynamoDB when report generation completes.
    
    Parameters:
    - company_id: str
    - company_name: str
    - job_id: str
    - preprocessed_json_path: S3 path to preprocessed JSON data
    - image2text_path: S3 path to image2text extraction data
    
    Returns:
    - bool: True if workflow was triggered successfully
    
    Raises:
    - ValueError: For permanent errors (invalid data, configuration issues)
    - Exception: For transient errors (timeouts, temporary network issues)
    """
    if not N8N_REPORT_WEBHOOK_URL:
        logger.error("[n8n] N8N_REPORT_WEBHOOK_URL not configured")
        raise ValueError("Permanent error: N8N report webhook URL not configured")
    
    payload = {
        "companyId": company_id,
        "companyName": safe_name(company_name),
        "jobId": job_id,
        "bucket": BUCKET,
        "preprocessedJsonPath": preprocessed_json_path,
        "image2textPath": image2text_path,
        "timestamp": datetime.now(timezone.utc).isoformat()
    }
    
    try:
        logger.info(f"[n8n] Triggering report workflow for job {job_id} (fire-and-forget)")
        
        response = requests.post(
            N8N_REPORT_WEBHOOK_URL,
            json=payload,
            timeout=5,  # Short timeout - just confirm trigger accepted
            headers={"Content-Type": "application/json"}
        )
        response.raise_for_status()
        
        logger.info(f"[n8n] Report workflow triggered successfully for job {job_id}")
        return True  # Just confirm trigger succeeded
        
    except requests.exceptions.Timeout:
        # Timeout is OK for fire-and-forget - webhook was triggered
        logger.info(f"[n8n] Webhook triggered for job {job_id} (timed out waiting for response, but that's expected)")
        return True

    except requests.exceptions.ConnectionError as e:
        logger.error(f"[n8n] Connection error: {e}")
        raise Exception("Transient error: Failed to connect to n8n workflow")

    except requests.exceptions.HTTPError as e:
        status_code = e.response.status_code if e.response else None
        # 4xx errors are typically permanent (bad request, unauthorized, etc.)
        if status_code and 400 <= status_code < 500:
            logger.error(f"[n8n] HTTP {status_code} error: {e}")
            raise ValueError(f"Permanent error: n8n workflow returned HTTP {status_code}")
        # 5xx errors are typically transient (server issues)
        else:
            logger.error(f"[n8n] HTTP error: {e}")
            raise Exception(f"Transient error: n8n workflow HTTP error")

    except ValueError:
        # Re-raise ValueError as-is (permanent errors)
        raise

    except Exception as e:
        logger.error(f"[n8n] Unexpected error triggering workflow: {e}")
        raise Exception(f"Transient error: {str(e)}")

