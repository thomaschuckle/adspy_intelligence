# Lambda Handler
# -------------------------------
def lambda_handler(event, context):
    """
    Main Lambda handler for report generation.
    
    Expected API Gateway event format:
    - pathParameters: { "jobId": "..." }
    
    Response format:
    {
        "reportStatus": int,      # 0/1/2/-1
        "reportPath": str,        # S3 key (if status=2)
        "message": str,           # Status/error message (always present)
        "retryCount": int,        # Current retry count
        "dataCheckRetryCount": int, # Data availability retry count
        "permanent": bool,        # True if permanently failed
        "retrying": bool,         # True if will retry
        "scraperStatus": int,     # Prerequisite status
        "image2textStatus": int   # Prerequisite status (if relevant)
    }
    
    Note: No presigned URL generated here - use fetchReport API for that.
    """
    try:
        # =====================================
        # VALIDATION
        # =====================================
        job_id = event.get('pathParameters', {}).get('jobId')
        if not job_id or not isinstance(job_id, str) or not job_id.strip():
            logger.warning("[validation] Missing or invalid jobId")
            return {
                "statusCode": 400,
                "body": json.dumps({
                    "error": "Invalid or missing jobId",
                    "message": "jobId is required in path parameters"
                })
            }
        
        # Fetch job from DynamoDB
        try:
            response = jobs_table.get_item(Key={'jobId': job_id})
            job_item = response.get('Item')
        except Exception as e:
            logger.error(f"[dynamodb] Error fetching job {job_id}: {e}")
            return {
                "statusCode": 500,
                "body": json.dumps({
                    "error": "Database error fetching job",
                    "message": f"Failed to retrieve job {job_id} from database"
                })
            }
        
        if not job_item:
            logger.warning(f"[validation] Job not found: {job_id}")
            return {
                "statusCode": 404,
                "body": json.dumps({
                    "error": "Job not found",
                    "message": f"No job exists with ID {job_id}"
                })
            }
        
        company_id = job_item.get('companyId')
        if not company_id or not isinstance(company_id, str) or not company_id.strip():
            logger.error(f"[validation] Invalid companyId in job {job_id}")
            return {
                "statusCode": 400,
                "body": json.dumps({
                    "error": "Invalid companyId in job data",
                    "message": "Job data is corrupted - missing or invalid companyId"
                })
            }
        
        report_status = int(job_item.get('reportStatus', 0))
        report_path = job_item.get('reportPath', '').strip()
        scraper_status = int(job_item.get('scraperStatus', 0))
        image2text_status = int(job_item.get('image2textStatus', 0))
        
        # =====================================
        # GUARD: Report Already Exists
        # =====================================
        if report_path:
            logger.info(f"[guard] Report already exists for job {job_id}: {report_path}")
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": 2,
                    "reportPath": report_path,
                    "message": "Report already generated for this job"
                })
            }
        
        # =====================================
        # GUARD: Data Inconsistency Check
        # =====================================
        if report_status == 2 and not report_path:
            logger.error(f"[error] Job {job_id} has reportStatus=2 but no reportPath")
            return {
                "statusCode": 500,
                "body": json.dumps({
                    "error": "Data inconsistency",
                    "message": "Report marked as complete but no report file found in database"
                })
            }
        
        # =====================================
        # GUARD: Permanent Failure
        # =====================================
        if report_status == -1:
            logger.info(f"[guard] Job {job_id} permanently failed")
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": -1,
                    "message": "Report generation permanently failed",
                    "retryCount": int(job_item.get('reportRetryCount', 0)),
                    "permanent": True
                }, default=decimal_default)
            }
        
        # =====================================
        # GUARD: Check image2text Status
        # =====================================
        # Check if image2text failed or completed while we were waiting
        if report_status == 1:
            report_started_at = job_item.get('reportStartedAt')
            
            # Only reset if workflow hasn't been triggered yet
            if not report_started_at:
                if image2text_status == 2:
                    # Image2text completed - reset to allow report generation
                    logger.info(f"[guard] Image extraction completed, resetting reportStatus to 0 for job {job_id}")
                    try:
                        jobs_table.update_item(
                            Key={'jobId': job_id},
                            UpdateExpression="SET reportStatus = :status",
                            ExpressionAttributeValues={':status': 0}
                        )
                        report_status = 0  # Update local variable
                    except Exception as db_error:
                        logger.error(f"[dynamodb] Failed to reset reportStatus: {db_error}")
                
                elif image2text_status == -1:
                    # Image2text failed permanently
                    logger.info(f"[guard] Image extraction failed permanently for job {job_id}")
                    try:
                        jobs_table.update_item(
                            Key={'jobId': job_id},
                            UpdateExpression="SET reportStatus = :status",
                            ExpressionAttributeValues={':status': 0}
                        )
                        report_status = 0  # Reset to allow retry/empty report generation
                    except Exception as db_error:
                        logger.error(f"[dynamodb] Failed to reset reportStatus: {db_error}")
                
                elif image2text_status == 0:
                    # Image2text reset to 0 (likely failed) - reset report status
                    logger.info(f"[guard] Image extraction reset to 0 (likely failed) for job {job_id}")
                    try:
                        jobs_table.update_item(
                            Key={'jobId': job_id},
                            UpdateExpression="SET reportStatus = :status",
                            ExpressionAttributeValues={':status': 0}
                        )
                        report_status = 0  # Update local variable
                    except Exception as db_error:
                        logger.error(f"[dynamodb] Failed to reset reportStatus: {db_error}")
        
        # =====================================
        # GUARD: In Progress
        # =====================================
        if report_status == 1:
            logger.info(f"[guard] Report generation in progress for job {job_id}")
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": 1,
                    "message": "Report generation in progress - please continue polling"
                })
            }
        
        # =====================================
        # GUARD: Check Scraping Prerequisite
        # =====================================
        # Scraping must complete successfully before report generation
        if scraper_status == -1:
            # Scraping failed permanently - inherit failure
            logger.info(f"[guard] Scraping failed for job {job_id}, inheriting failure")
            try:
                jobs_table.update_item(
                    Key={'jobId': job_id},
                    UpdateExpression="SET reportStatus = :status",
                    ExpressionAttributeValues={':status': -1}
                )
            except Exception as db_error:
                logger.error(f"[dynamodb] Failed to set reportStatus=-1: {db_error}")
            
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": -1,
                    "message": "Cannot generate report - scraping failed permanently",
                    "scraperStatus": -1,
                    "permanent": True
                })
            }
        
        if scraper_status != 2:
            # Scraping not complete yet (status 0 or 1)
            logger.info(f"[guard] Scraping not complete for job {job_id} (status={scraper_status})")
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": 0,
                    "message": "Cannot generate report - scraping not complete yet (please wait for scraping to finish)",
                    "scraperStatus": scraper_status
                })
            }
        
        # At this point, scraper_status = 2 (scraping completed successfully)
        
        # =====================================
        # Fetch Company Data
        # =====================================
        try:
            company_response = companies_table.get_item(Key={'companyId': company_id})
            company_item = company_response.get('Item')
        except Exception as e:
            logger.error(f"[dynamodb] Error fetching company {company_id}: {e}")
            return {
                "statusCode": 500,
                "body": json.dumps({
                    "error": "Database error fetching company",
                    "message": f"Failed to retrieve company {company_id} from database"
                })
            }
        
        if not company_item:
            logger.error(f"[validation] Company not found: {company_id}")
            return {
                "statusCode": 404,
                "body": json.dumps({
                    "error": "Company not found",
                    "message": f"No company exists with ID {company_id}"
                })
            }
        
        company_name = company_item.get('companyName', '').strip().lower()
        safe_company = safe_name(company_name)
        num_ads = int(company_item.get('numAds', 0))
        
        # =====================================
        # Define S3 Paths
        # =====================================
        preprocessed_json_path = f"preprocessed/json/{safe_company}/"
        image2text_path = f"image2text/{safe_company}/"
        
        # =====================================
        # GUARD: Check image2text In Progress
        # =====================================
        # If image2text is in progress, set reportStatus=1 so UI continues polling
        if image2text_status == 1:
            # Only set reportStatus=1 if it's not already 1
            if report_status != 1:
                logger.info(f"[guard] Image extraction in progress for job {job_id}, setting reportStatus=1")
                try:
                    jobs_table.update_item(
                        Key={'jobId': job_id},
                        UpdateExpression="SET reportStatus = :status",
                        ExpressionAttributeValues={':status': 1}
                    )
                except Exception as db_error:
                    logger.error(f"[dynamodb] Failed to set reportStatus=1: {db_error}")
            
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": 1,
                    "message": "Waiting for image extraction to complete - please continue polling",
                    "image2textStatus": 1
                })
            }
        
        # =====================================
        # Check S3 Data Availability
        # =====================================
        has_usable_json = check_s3_folder_has_usable_data(preprocessed_json_path, check_json_content=True)
        has_image2text = check_s3_folder_has_usable_data(image2text_path, check_json_content=False)
        
        # =====================================
        # Handle No Data Scenario
        # =====================================
        if not has_usable_json and not has_image2text:
            # No usable data found - implement retry logic
            data_check_retry_count = int(job_item.get('dataCheckRetryCount', 0))
            
            logger.warning(f"[guard] No usable data for job {job_id} (attempt {data_check_retry_count + 1}/{MAX_DATA_CHECK_RETRIES})")
            
            if data_check_retry_count < MAX_DATA_CHECK_RETRIES:
                # Increment retry counter and return for next poll
                try:
                    jobs_table.update_item(
                        Key={'jobId': job_id},
                        UpdateExpression="SET dataCheckRetryCount = :rc",
                        ExpressionAttributeValues={':rc': data_check_retry_count + 1}
                    )
                except Exception as db_error:
                    logger.error(f"[dynamodb] Failed to increment dataCheckRetryCount: {db_error}")
                
                return {
                    "statusCode": 200,
                    "body": json.dumps({
                        "reportStatus": 0,
                        "message": f"Data not yet available - will retry (attempt {data_check_retry_count + 1}/{MAX_DATA_CHECK_RETRIES})",
                        "dataCheckRetryCount": data_check_retry_count + 1,
                        "retrying": True
                    }, default=decimal_default)
                }
            else:
                # Max data check retries exceeded - generate empty report
                logger.info(f"[empty_report] Generating empty report for job {job_id} after {data_check_retry_count} data check retries")
                
                reason = "No ad data available after scraping"
                if num_ads == 0:
                    reason = "Company has no ads"
                
                empty_report = generate_empty_report(company_name, num_ads, reason)
                
                # Save empty report to S3
                report_path = f"reports/{job_id}/report.json"
                try:
                    s3.put_object(
                        Bucket=BUCKET,
                        Key=report_path,
                        Body=json.dumps(empty_report, ensure_ascii=False, indent=2),
                        ContentType='application/json'
                    )
                    logger.info(f"[s3] Saved empty report to: {report_path}")
                except Exception as e:
                    logger.error(f"[s3] Failed to save empty report {report_path}: {e}")
                    return {
                        "statusCode": 500,
                        "body": json.dumps({
                            "error": "Failed to save empty report to S3",
                            "message": f"Empty report generation failed: {str(e)}"
                        })
                    }
                
                # Update job with completion
                try:
                    jobs_table.update_item(
                        Key={'jobId': job_id},
                        UpdateExpression="SET reportStatus = :status, reportPath = :path, dataCheckRetryCount = :rc",
                        ExpressionAttributeValues={
                            ':status': 2,
                            ':path': report_path,
                            ':rc': 0  # Reset retry count
                        }
                    )
                    logger.info(f"[complete] Empty report generated for job {job_id}")
                except Exception as e:
                    logger.error(f"[dynamodb] Failed to update job with empty report: {e}")
                    return {
                        "statusCode": 500,
                        "body": json.dumps({
                            "error": "Empty report generated but failed to update job status",
                            "message": f"Report saved to {report_path} but database update failed: {str(e)}",
                            "reportPath": report_path
                        })
                    }
                
                return {
                    "statusCode": 200,
                    "body": json.dumps({
                        "reportStatus": 2,
                        "reportPath": report_path,
                        "message": f"Empty report generated - {reason}"
                    })
                }
        
        # =====================================
        # Data Available - Proceed with Report Generation
        # =====================================
        logger.info(f"[check] Found usable data for job {job_id} (json={has_usable_json}, image2text={has_image2text})")
        
        # Reset data check retry count since we found data
        try:
            jobs_table.update_item(
                Key={'jobId': job_id},
                UpdateExpression="SET dataCheckRetryCount = :rc",
                ExpressionAttributeValues={':rc': 0}
            )
        except Exception as db_error:
            logger.error(f"[dynamodb] Failed to reset dataCheckRetryCount: {db_error}")
        
        # =====================================
        # Set Status to In Progress
        # =====================================
        now_iso = datetime.now(timezone.utc).isoformat()
        
        try:
            jobs_table.update_item(
                Key={'jobId': job_id},
                UpdateExpression="SET reportStatus = :status, reportStartedAt = :ts",
                ExpressionAttributeValues={':status': 1, ':ts': now_iso}
            )
            logger.info(f"[status] Set reportStatus=1 for job {job_id}")
        except Exception as e:
            logger.error(f"[dynamodb] Failed to set reportStatus=1: {e}")
            return {
                "statusCode": 500,
                "body": json.dumps({
                    "error": "Database error",
                    "message": "Failed to update job status before report generation"
                })
            }
        
        # =====================================
        # Trigger N8N Report Workflow
        # =====================================
        try:
            logger.info(f"[workflow] Triggering report generation for job {job_id}")
            trigger_report_workflow(
                company_id=company_id,
                company_name=company_name,
                job_id=job_id,
                preprocessed_json_path=preprocessed_json_path,
                image2text_path=image2text_path
            )
            
            # Workflow triggered successfully
            # n8n will update reportStatus, reportPath, and reset retry counts when done
            logger.info(f"[workflow] Report workflow triggered for job {job_id}, n8n will update status when complete")
            
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": 1,
                    "message": "Report generation started - please continue polling"
                })
            }
            
        except ValueError as e:
            # Permanent error - couldn't trigger workflow
            retry_count = int(job_item.get('reportRetryCount', 0))
            logger.error(f"[workflow] Permanent error triggering workflow for job {job_id}: {e}")
            
            try:
                jobs_table.update_item(
                    Key={'jobId': job_id},
                    UpdateExpression="SET reportStatus = :status, reportRetryCount = :rc",
                    ExpressionAttributeValues={':status': -1, ':rc': retry_count}
                )
            except Exception as db_error:
                logger.error(f"[dynamodb] Failed to update job status after permanent error: {db_error}")
            
            return {
                "statusCode": 200,
                "body": json.dumps({
                    "reportStatus": -1,
                    "message": f"Failed to trigger report generation: {str(e)}",
                    "permanent": True,
                    "retryCount": retry_count
                }, default=decimal_default)
            }
            
        except Exception as e:
            # Transient error - apply retry logic
            current_retry_count = int(job_item.get('reportRetryCount', 0))
            logger.error(f"[workflow] Transient error for job {job_id} (attempt {current_retry_count + 1}/{MAX_WORKFLOW_RETRIES}): {e}")
            
            if current_retry_count >= MAX_WORKFLOW_RETRIES:
                # Max retries exceeded - don't increment further
                try:
                    jobs_table.update_item(
                        Key={'jobId': job_id},
                        UpdateExpression="SET reportStatus = :status",
                        ExpressionAttributeValues={':status': -1}
                    )
                except Exception as db_error:
                    logger.error(f"[dynamodb] Failed to update job status after max retries: {db_error}")
                
                return {
                    "statusCode": 200,
                    "body": json.dumps({
                        "reportStatus": -1,
                        "message": f"Failed to trigger report generation after {MAX_WORKFLOW_RETRIES} attempts: {str(e)}",
                        "permanent": True,
                        "retryCount": current_retry_count
                    }, default=decimal_default)
                }
            else:
                # Still have retries left - increment and schedule retry
                new_retry_count = current_retry_count + 1
                logger.info(f"[workflow] Scheduling retry for job {job_id} (attempt {new_retry_count}/{MAX_WORKFLOW_RETRIES})")
                try:
                    jobs_table.update_item(
                        Key={'jobId': job_id},
                        UpdateExpression="SET reportStatus = :status, reportRetryCount = :rc",
                        ExpressionAttributeValues={':status': 0, ':rc': new_retry_count}
                    )
                except Exception as db_error:
                    logger.error(f"[dynamodb] Failed to reset job status for retry: {db_error}")
                
                return {
                    "statusCode": 200,
                    "body": json.dumps({
                        "reportStatus": 0,
                        "message": f"Failed to trigger report generation, will retry (attempt {new_retry_count}/{MAX_WORKFLOW_RETRIES}): {str(e)}",
                        "retrying": True,
                        "retryCount": new_retry_count
                    }, default=decimal_default)
                }
        
    except Exception as e:
        # Catch-all for unexpected errors
        logger.exception(f"[error] Unhandled exception in reportHandler: {e}")
        return {
            "statusCode": 500,
            "body": json.dumps({
                "error": "Internal server error",
                "message": f"Unexpected error occurred: {str(e)}"
            })
        }